export function buildDiagramHeaderView({
  featureFlags = {},
  shellProps = {},
  sid,
  saveDirtyHint,
  handleSaveCurrentTab,
  handleCreateRevisionAction,
  handleUndoAction,
  handleRedoAction,
  canUndo = false,
  canRedo = false,
  workbench,
  tab,
  isSwitchingTab,
  isFlushingTab,
  switchTab,
  hasSession,
  attentionOpen,
  toggleAttentionPanel,
  attentionItemsRaw,
  attentionItemsCount,
  doGenerate,
  toolbarMenuButtonRef,
  toggleToolbarMenu,
  toolbarMenuOpen,
  importInputRef,
  onImportPicked,
  hybridV2FileInputRef,
  handleHybridV2ImportFile,
  drawioFileInputRef,
  handleDrawioImportFile,
  topPanelsView,
  publishGitMirrorSnapshot,
  sessionPresenceView,
  remoteSaveHighlightView,
  diagramStateVersion = 0,
  saveUploadStatus,
  asArray,
} = {}) {
  const localDiagramStateVersion = Number.isFinite(Number(diagramStateVersion))
    ? Math.max(0, Number(diagramStateVersion))
    : 0;
  const serverVersion = Number.isFinite(Number(remoteSaveHighlightView?.serverVersion))
    ? Math.max(0, Number(remoteSaveHighlightView?.serverVersion))
    : 0;
  const saveUploadState = saveUploadStatus && typeof saveUploadStatus === "object" ? saveUploadStatus : {};
  const isConflictFromSave = String(saveUploadState.state || "").toLowerCase() === "conflict";
  const isConflictFromRemote = serverVersion > 0 && serverVersion > localDiagramStateVersion;
  return {
    ...shellProps,
    featureFlags,
    sessionId: sid,
    saveDirtyHint,
    handleSaveCurrentTab,
    handleCreateRevisionAction,
    handleUndoAction,
    handleRedoAction,
    canUndo: canUndo === true,
    canRedo: canRedo === true,
    workbench,
    tab,
    isSwitchingTab,
    isFlushingTab,
    switchTab,
    hasSession,
    attentionOpen,
    toggleAttentionPanel,
    attentionItemsRaw,
    attentionItemsCount,
    doGenerate,
    toolbarMenuButtonRef,
    toggleToolbarMenu,
    toolbarMenuOpen,
    importInputRef,
    onImportPicked,
    hybridV2FileInputRef,
    handleHybridV2ImportFile,
    drawioFileInputRef,
    handleDrawioImportFile,
    topPanelsView,
    publishGitMirrorSnapshot,
    sessionPresenceView,
    diagramStateVersion: localDiagramStateVersion,
    diagramStateConflict: isConflictFromSave || isConflictFromRemote,
    diagramStateConflictServerVersion: serverVersion,
    diagramStateConflictActorLabel: String(remoteSaveHighlightView?.actorLabel || "").trim(),
    asArray,
  };
}

export function buildDiagramControlsView(view = {}) {
  const sections = buildDiagramControlsSections(view);
  return {
    ...view,
    sections,
  };
}
